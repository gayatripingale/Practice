# command to find the sum(total) of the 'value's
# of the 'keys' in the dictionary {key:value}

# use -> sum(dictionary_name.values()) to find the sum

num_dict= {'a':2,'b':9,'c':14,'d':5,'e':6}

total = sum(num_dict.values())

print("The number dictionary is:", num_dict)

print("And the total of its values is:", total)


